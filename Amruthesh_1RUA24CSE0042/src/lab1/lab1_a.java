package lab1;

public class lab1_a {

	public static void main(String[] args) {
		System.out.println("Using for loop: ");
		for(int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println();
		System.out.println("Using while loop: ");
		int n = 1;
		while(n <= 10) {
			System.out.println(n);
			n++;
		}
	}

}
